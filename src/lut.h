static inline int32_t lerp(const int32_t * lut, uint32_t x, int s0, int s1) {
	uint32_t idx = (x >> s0) << 1;
	int32_t frac = x & ((1 << s0) - 1);
	int32_t y = lut[idx];
	int32_t dy = lut[idx + 1];
	return y + ((dy * (frac >> s1)) >> (s0 - s1));
}

