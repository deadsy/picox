#ifndef QFMT_H
#define QFMT_H

//-----------------------------------------------------------------------------

#include <inttypes.h>

//-----------------------------------------------------------------------------
// Q1.31

static inline float q31_to_float(int32_t x) {
	return (float)x *(1.f / (float)(1UL << 31));
}

static inline int16_t q31_to_q15(int32_t x) {
	return (int16_t) (x >> 16);
}

//-----------------------------------------------------------------------------
// Q1.15

static inline float q15_to_float(int16_t x) {
	return (float)x *(1.f / (float)(1UL << 15));
}

//-----------------------------------------------------------------------------

#endif				// QFMT_H

